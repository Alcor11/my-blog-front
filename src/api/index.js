import request from '@/utils/request'


export function fetchList(params) {
    return request({
        url: 'api/article/list',
        method: 'post',
        data: params
    })
}

export function fetchContents(id) {
    return request({
        url: 'api/article/doc',
        method: 'get',
        params: {'id' : id }
    })
}

